import time

class SpeedMonitor():
	def __init__(self):
		self._t0 = None
		self._points = [ ]

	@property
	def data(self):
		return self._points

	def record(self, progress):
		now = time.time()
		if self._t0 is None:
			self._t0 = now
		tdiff = now - self._t0
		self._points.append((tdiff, progress))
